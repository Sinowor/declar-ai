import { Database as SqlJsDatabase } from 'sql.js'

function qOne(db: SqlJsDatabase, sql: string, params: any[] = []): any | null {
  const stmt = db.prepare(sql)
  if (params.length > 0) stmt.bind(params)
  let result: any = null
  if (stmt.step()) result = stmt.getAsObject()
  stmt.free()
  return result
}

const seedIfEmpty = (db: SqlJsDatabase, table: string, data: any[][], cols?: string[], placeholders?: string) => {
  const c = qOne(db, `SELECT COUNT(*) as cnt FROM ${table}`) as any
  if (c && c.cnt === 0) {
    const colStr = cols ? `(${cols.join(', ')})` : '(code, name)'
    const valStr = placeholders || '(?, ?)'
    for (const row of data) {
      db.run(`INSERT INTO ${table} ${colStr} VALUES ${valStr}`, row)
    }
  }
}

